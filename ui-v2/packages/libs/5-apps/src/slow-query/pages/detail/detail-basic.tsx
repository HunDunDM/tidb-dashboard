import {
  formatTime,
  formatValue,
} from "@pingcap-incubator/tidb-dashboard-lib-utils"
import { useMemo } from "react"

import { InfoModel, InfoTable } from "../../components/info-table"
import { SlowqueryModel } from "../../models"

function getData(data: SlowqueryModel): InfoModel[] {
  return [
    {
      name: "Finish Time",
      value: formatTime(data.timestamp! * 1000),
      desc: "The time this query finished execution",
    },
    {
      name: "Query Template ID",
      value: data.digest!,
      desc: "a.k.a. Query digest",
    },
    {
      name: "Is Internal?",
      value: data.is_internal === 1 ? "Yes" : "No",
      desc: "Whether this is an internal query",
    },
    {
      name: "Is Success?",
      value: data.success === 1 ? "Yes" : "No",
      desc: "Whether query is executed successfully",
    },
    {
      name: "Is Prepared?",
      value: data.prepared === 1 ? "Yes" : "No",
      desc: "Is Generated by the prepare statement",
    },
    {
      name: "Is Plan from Cache?",
      value: data.plan_from_cache === 1 ? "Yes" : "No",
    },
    {
      name: "Is Plan from Binding?",
      value: data.plan_from_binding === 1 ? "Yes" : "No",
    },
    {
      name: "Execution Database",
      value: data.db || "-",
      desc: "The database used to execute the query",
    },
    {
      name: "Index Names",
      value: data.index_names || "-",
      desc: "The name of the used index",
    },
    {
      name: "Used Statistics",
      value: data.stats || "-",
    },
    {
      name: "Backoff Types",
      value: data.backoff_types || "-",
    },
    {
      name: "Max Memory",
      value: formatValue(data.memory_max || 0, "bytes"),
      desc: "Maximum memory usage of the query",
    },
    {
      name: "Max Disk",
      value: formatValue(data.disk_max || 0, "bytes"),
      desc: "Maximum disk usage of the query",
    },
    {
      name: "TiDB Instance",
      value: data.instance || "-",
      desc: "The TiDB address that handles the query",
    },
    {
      name: "Connection ID",
      value: data.connection_id || "-",
      desc: "Unique connection ID of the query",
    },
    {
      name: "Execution User",
      value: data.user || "-",
      desc: "The user that executes the query",
    },
    {
      name: "Client Address",
      value: data.host || "-",
      desc: "The address of the client that sends the query",
    },
  ]
}

export function DetailBasic({ data }: { data: SlowqueryModel }) {
  const infoData = useMemo(() => getData(data), [data])
  return <InfoTable data={infoData} />
}
